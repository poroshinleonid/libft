#include "ft_printf_bonus.h"

char	byte_to_hex_2_b(unsigned char *c)
{
	char	res;

	if ((*c >> 4) < 10)
		res = (*c >> 4) + '0';
	else
		res = (*c >> 4) + 'a' - 10;
	return (res);
}
