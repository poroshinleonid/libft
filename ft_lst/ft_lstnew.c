#include "../libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*head;

	if (!content)
		return (NULL);
	head = (t_list *)malloc(sizeof(t_list));
	if (!head)
		return (NULL);
	head->content = content;
	head->next = NULL;
	return (head);
}
